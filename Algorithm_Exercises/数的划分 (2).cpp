/*
问题描述
　　将整数n分成k份，且每份不能为空，任意两份不能相同(不考虑顺序)。
　　例如：n=7，k=3，下面三种分法被认为是相同的。
　　1，1，5; 1，5，1; 5，1，1;
　　问有多少种不同的分法。
输入格式
　　n，k
输出格式
　　一个整数，即不同的分法
样例输入
7 3
样例输出
4 {四种分法为：1，1，5;1，2，4;1，3，3;2，2，3;}
数据规模和约定
　　6<n<=200，2<=k<=6
*/
#include<iostream>
using namespace std;  
int main()
{
    int n,k;
    int ans[300][300];
    cin>>n>>k;
    for(int i=1;i<=n;i++)
    {
        for(int j=1;j<=k;j++)
        {
            if(i<j)
                ans[i][j]=0;
            else if(i==j)
                ans[i][j]=1;
            else
                ans[i][j]=ans[i-j][j]+ans[i-1][j-1];
        }
    }
    cout<<ans[n][k]<<endl;
}
